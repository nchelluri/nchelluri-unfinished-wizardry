package org.simpletext;

public class Util {
	public static String[] strings(String... elements) {
		String[] array = new String[0];

		if (elements != null) {
			array = elements;
		}

		return array;
	}

	public static char[] chars(char... elements) {
		char[] array = new char[0];

		if (elements != null) {
			array = elements;
		}

		return array;
	}
}
